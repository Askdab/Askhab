package com.company;

public class minmax {
    static int min(int a, int b) {
        if (a < b) return a;
        else
            return b;
    }

    static long min(long a, long b) {
        if (a < b) return a;
        else
            return b;
    }

    static double min(double a, double b) {
        if (a < b) return a;
        else
            return b;
    }

    static int max(int a, int b) {
        if (a > b) return a;
        else
            return b;
    }

    static long max(long a, long b) {
        if (a > b) return a;
        else
            return b;
    }

    static double max(double a, double b) {
        if (a > b) return a;
        else
            return b;
    }
}
